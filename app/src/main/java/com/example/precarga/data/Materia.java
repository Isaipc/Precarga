package com.example.precarga.data;

public class Materia {
    public String nombre;
    public String clave;
    public int creditos;
    public String tipo;

    public Materia(String nombre, String clave, int creditos, String tipo) {
        this.nombre = nombre;
        this.clave = clave;
        this.creditos = creditos;
        this.tipo = tipo;
    }
}
